  def research(self, user_prompt: str):
    print(f"[researcher] Starting research for: {user_prompt}")

    # search the web
    query = self.create_search_query(user_prompt)
    results = self.web_search(query)
    if not results:
      return "No results found."
    filtered = self.filter_results(results, user_prompt)
    if not filtered:
      filtered = results[:3]  # fallback

    # crawl and summarize
    summaries = []
    for res in filtered:
      url = res["url"]
      text = self.crawl_webpage(url)
      if not text or len(text) < 200:
        continue

      summary = self.summarize_page(text)
      summaries.append({
        "title": res["title"],
        "url": url,
        "summary": summary
      })

    if not summaries:
      return "Failed to extract useful information."

    # final answer
    final_prompt = f"""
    You are a senior researcher.

    User question:
    {user_prompt}

    Here are summarized sources:
    {json.dumps(summaries, indent=2)}

    Provide a well-structured, accurate answer based on the sources.
    Include references to sources where appropriate.
    """

    response = ollama.chat(
      model=self.model,
      messages=[{"role": "user", "content": final_prompt}]
    )
    return response["message"]["content"]
